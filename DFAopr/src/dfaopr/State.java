package dfaopr;

/**
 *
 * @author Junaid
 */
public class State {
    int x;
    int y;

    public State() {
        
    }

    public State(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "State{" + "x=" + x + ", y=" + y + '}';
    }
    
    
    
    
    
    
}
